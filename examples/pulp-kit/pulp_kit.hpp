#pragma once

// PulpKit -- a circuit-modelled analog drum machine.
//
// One polyphonic Processor that owns one instance of every kit voice and routes
// a MIDI note-on to the voice that lives on that note. Every voice is a struck
// or gated circuit model (never a sample), so the kit is one persistent
// instrument rather than a bank of one-shots: each drum rings in its own state
// and a repeated hit superposes onto the tail already in flight.
//
// The topology is fixed -- one note map, no runtime routing -- so this is a
// Processor, not a graph. Polyphony across the voices is a Processor concern;
// every voice advances every sample and the block sum is the mix.
//
// The note map mirrors the classic analog-drum layout:
//   36 kick   37 rim   38/40 snare   39 clap
//   41/43 low tom   45/47 mid tom   48/50 hi tom
//   42/44 closed hat (chokes the open hat)   46 open hat   49 cymbal
//   51/56 cowbell   70 maracas   75 clave
//
// Control surface: every voice exposes exactly four parameters -- Level, Tune,
// Decay, Tone -- in that order, grouped voice-by-voice so a host shows the same
// four knobs per sound every time. Each maps onto that voice's uniform
// set_level / set_tune / set_decay / set_tone. The stock knob positions (Level
// 100%, Tune 1.0, Decay 50%, Tone 50%) reproduce the calibrated reference sound
// exactly; the controls only open musical range around it. The snare keeps its
// tone/snap Balance as a fifth, voice-specific control.
//
// Voices calibrated to the measured output of the reference instrument; the
// sound is generated by the circuit models sample by sample. No reference audio
// is embedded.

