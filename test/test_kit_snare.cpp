// Tests for the kit's snare and rimshot/clave voices. These are struck
// resonators plus (for the snare) a filtered-noise snappy, so what matters is
// what they actually emit: the snare must carry BOTH a tonal shell and a
// broadband noise crack, its balance knob must move real energy between the
// two, and the rim/clave must be a short bright tick whose ring pitch is a real,
// controllable resonance. Every claim here is a rendered-and-measured number.
//
// Reference targets are measured constants from the reference drum machine's
// pads (offline renders); no reference audio is stored, and every sample is
// generated by the circuit model. The voices are calibrated to those numbers.

#include <catch2/catch_approx.hpp>
#include <catch2/catch_test_macros.hpp>

#include "harness/rt_allocation_probe.hpp"
