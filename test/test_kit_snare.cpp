constexpr double kFs = 48000.0;

/// The same five numbers the reference pads were measured with: peak, the
/// −60 dB decay, the zero-cross rate over the active region, and the onset
/// spectral centroid and flatness. Centroid/flatness are read from a
/// Hann-windowed magnitude spectrum over a 32768-sample window at the onset,
/// matching how the reference targets were taken.
struct SpectralMetrics {
    double peak = 0.0;
    double t60_s = 0.0;
    double zcr_hz = 0.0;
    double centroid_hz = 0.0;
    double flatness = 0.0;  ///< 0 = pure tone, → 1 = white noise.
};

SpectralMetrics measure(std::span<const float> x, double fs) {
    SpectralMetrics m;
    const std::size_t n = x.size();
    std::size_t peak_i = 0;
    for (std::size_t i = 0; i < n; ++i) {
        const float a = std::fabs(x[i]);
        if (a > m.peak) {
            m.peak = a;
            peak_i = i;
        }
    }
    const float thr = static_cast<float>(m.peak / 1000.0);
    std::size_t last = peak_i;
    for (std::size_t i = 0; i < n; ++i)
        if (std::fabs(x[i]) > thr) last = i;
    m.t60_s = static_cast<double>(last) / fs;

    if (last > peak_i + 1) {
        std::size_t zc = 0;
        for (std::size_t i = peak_i + 1; i <= last; ++i)
            if ((x[i - 1] >= 0.0f) != (x[i] >= 0.0f)) ++zc;
        m.zcr_hz = static_cast<double>(zc) / (static_cast<double>(last - peak_i) / fs) * 0.5;
    }

    const int fft_length = 32768;
    const std::size_t avail = (n > peak_i) ? (n - peak_i) : 0;
    const std::size_t seg = std::min<std::size_t>(fft_length, avail);
    if (seg >= 4096) {
        std::vector<float> buf(x.begin() + static_cast<std::ptrdiff_t>(peak_i),
                               x.begin() + static_cast<std::ptrdiff_t>(peak_i + seg));
        buf.resize(fft_length, 0.0f);
        float* ch0 = buf.data();
        pulp::audio::BufferView<const float> bv(&ch0, 1, fft_length);
        ResponseOptions ro;
        ro.fft_length = fft_length;
        ro.window = Window::hann;
        const double checks[1] = {1000.0};
        const auto curve = magnitude_spectrum_curve(bv, fs, std::span<const double>(checks, 1), ro);
        double num = 0.0, den = 0.0, log_sum = 0.0, lin_sum = 0.0;
        int cnt = 0;
        for (const auto& p : curve.full) {
            if (p.hz < 20.0 || p.hz > 20000.0) continue;
            const double lin = std::pow(10.0, p.magnitude_db / 20.0);
            num += p.hz * lin;
            den += lin;
            log_sum += std::log(lin + 1e-12);
            lin_sum += lin;
            ++cnt;
        }
        if (den > 0.0) m.centroid_hz = num / den;
        if (cnt > 0 && lin_sum > 0.0) m.flatness = std::exp(log_sum / cnt) / (lin_sum / cnt);
    }
    return m;
}

/// Render one snare hit. Balance/velocity as given; the hit lands 10 ms in so
/// the onset window is clean and the tail rings out inside the buffer.
std::vector<float> render_snare(double balance, double velocity = 1.0, double duration_s = 1.0) {
    SnareVoice voice;
    voice.prepare(kFs);
    voice.set_balance(balance);
    std::vector<float> y(static_cast<std::size_t>(duration_s * kFs), 0.0f);
    const int hit = static_cast<int>(0.01 * kFs);
    for (int n = 0; n < static_cast<int>(y.size()); ++n) {
        if (n == hit) voice.trigger(velocity);
        y[static_cast<std::size_t>(n)] = voice.process();
    }
    return y;
}

std::vector<float> render_rim(double tune, double decay, double velocity = 1.0,
                              double duration_s = 0.3) {
    RimClaveVoice voice;
    voice.prepare(kFs);
    voice.set_tune(tune);
    voice.set_decay(decay);
    std::vector<float> y(static_cast<std::size_t>(duration_s * kFs), 0.0f);
    const int hit = static_cast<int>(0.01 * kFs);
    for (int n = 0; n < static_cast<int>(y.size()); ++n) {
        if (n == hit) voice.trigger(velocity);
        y[static_cast<std::size_t>(n)] = voice.process();
    }
    return y;
}

